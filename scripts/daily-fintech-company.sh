    "007|宜人金科|Yiren Digital|P2P 转型、财富管理|⭐"
    "008|拍拍贷|PPDAI|消费信贷、智能风控|⭐"
    "009|信也科技|FinVolution|国际信贷、AI 风控|⭐"
    "010|奇富科技|Qifu Technology|消费信贷、金融科技|⭐"
    "101|Stripe|Stripe|支付基础设施|⭐⭐⭐"
    "102|Square/Block|Block Inc|支付、加密货币|⭐⭐⭐"
    "103|PayPal|PayPal|数字支付|⭐⭐"
    "104|Affirm|Affirm|先买后付 (BNPL)|⭐⭐"
    "105|Klarna|Klarna|先买后付 (BNPL)|⭐⭐"
)

# 获取今日企业
TOTAL_COMPANIES=${#COMPANIES[@]}
TODAY_INDEX=$((CURRENT_INDEX % TOTAL_COMPANIES))
TODAY_COMPANY="${COMPANIES[$TODAY_INDEX]}"

# 解析企业信息
IFS='|' read -r SEQ_NUM NAME_EN NAME_CORE PRIORITY <<< "$TODAY_COMPANY"

echo "📊 今日分析企业：$NAME_EN (序号：$SEQ_NUM)"

# 更新状态文件
NEXT_INDEX=$(( (CURRENT_INDEX + 1) % TOTAL_COMPANIES ))
echo "{\"current_index\": $NEXT_INDEX, \"last_run\": \"$DATE\"}" > "$STATE_FILE"

# 搜索企业信息
echo "🔍 搜索 $NAME_EN 相关信息..."
SEARCH_QUERY="$NAME_EN $NAME_CORE fintech business model 2025 2026"
SEARCH_OUTPUT=$(cd "$WORKSPACE" && uv run skills/searxng/scripts/searxng.py search "$SEARCH_QUERY" -n 10 2>&1 || echo "搜索暂不可用")

