#!/bin/bash

# Fintech 企业每日分析报告
# 用途：每个工作日早晨 8:00 自动生成一家 Fintech 企业深度分析并推送到飞书
# 配置：工作日 8:00 执行（周一 - 周五）

set -e

WORKSPACE="/home/admin/.openclaw/workspace"
DATE=$(date +%Y-%m-%d)
WEEKDAY=$(date +%u)  # 1=周一，7=周日
TIMESTAMP=$(date +"%Y-%m-%d %H:%M")
REPORT_DIR="$WORKSPACE/fintech-companies"
REPORT_FILE="$REPORT_DIR/report-$DATE.md"
COMPANIES_FILE="$REPORT_DIR/COMPANIES.yaml"
STATE_FILE="$REPORT_DIR/.state.json"

echo "🏦 开始生成 Fintech 企业分析报告..."
echo "时间：$TIMESTAMP"

# 检查是否是工作日（1-5 为周一到周五）
if [ "$WEEKDAY" -gt 5 ]; then
    echo "⏸️  周末休息，跳过执行"
    exit 0
fi

# 创建报告目录
mkdir -p "$REPORT_DIR"

# 初始化或读取状态文件
if [ ! -f "$STATE_FILE" ]; then
    echo '{"current_index": 0, "last_run": null}' > "$STATE_FILE"
fi

# 读取当前索引
CURRENT_INDEX=$(cat "$STATE_FILE" | grep -o '"current_index": [0-9]*' | grep -o '[0-9]*')
if [ -z "$CURRENT_INDEX" ]; then
    CURRENT_INDEX=0
fi

